import XCTest
@testable import FluxTraining

final class LocalizedCopyTests: XCTestCase {
  func test_spanishIsBaseLanguage() {
    let copy = LocalizedCopy(language: .es)

    XCTAssertEqual(copy.text(.heroTitle), "Entrena con foco y seguimiento real")
    XCTAssertEqual(copy.text(.trainingTitle), "Entrenamiento")
    XCTAssertEqual(copy.text(.domainFilterLabel), "Dominio")
    XCTAssertEqual(copy.text(.domainOperations), "Operaciones")
    XCTAssertEqual(copy.text(.offlineSyncTitle), "Offline + Sync")
    XCTAssertEqual(copy.text(.trackEvent), "Registrar evento")
    XCTAssertEqual(copy.text(.emailField), "Correo")
    XCTAssertEqual(copy.text(.passwordField), "Contrasena")
    XCTAssertEqual(copy.text(.recoverByEmail), "Recuperar por email")
    XCTAssertEqual(copy.text(.recoverBySMS), "Recuperar por SMS")
    XCTAssertEqual(copy.text(.languageOptionSpanish), "ES")
    XCTAssertEqual(copy.text(.languageOptionEnglish), "EN")
    XCTAssertEqual(copy.text(.runtimeStateSectionTitle), "Estado enterprise por dominio")
    XCTAssertEqual(copy.text(.runtimeStateModeLabel), "Modo runtime")
    XCTAssertEqual(copy.text(.runtimeStateRecoveryAction), "Recuperar dominio")
    XCTAssertEqual(copy.text(.roleLabel), "Rol")
    XCTAssertEqual(copy.text(.roleAthlete), "Atleta")
    XCTAssertEqual(copy.text(.roleCoach), "Coach")
    XCTAssertEqual(copy.text(.roleAdmin), "Admin")
    XCTAssertEqual(copy.text(.settingsTitle), "Ajustes")
    XCTAssertEqual(copy.text(.legalSectionTitle), "Privacidad y consentimiento")
    XCTAssertEqual(copy.text(.acceptPrivacyPolicy), "Acepto politica de privacidad")
    XCTAssertEqual(copy.humanStatus("signed_out"), "sin sesion")
    XCTAssertEqual(copy.humanStatus("validation_error"), "error de validacion")
    XCTAssertEqual(copy.humanStatus("consent_required"), "consentimiento requerido")
    XCTAssertEqual(copy.humanStatus("recovery_sent_email"), "recuperacion enviada por email")
    XCTAssertEqual(copy.humanStatus("recovery_sent_sms"), "recuperacion enviada por sms")
    XCTAssertEqual(copy.humanStatus("exported"), "exportado")
    XCTAssertEqual(copy.humanStatus("loading"), "cargando")
    XCTAssertEqual(copy.humanStatus("offline"), "sin conexion")
    XCTAssertEqual(copy.humanStatus("denied"), "sin permiso")
  }

