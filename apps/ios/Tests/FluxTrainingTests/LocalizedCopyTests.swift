    XCTAssertEqual(copy.text(.emailField), "Email")
    XCTAssertEqual(copy.text(.passwordField), "Password")
    XCTAssertEqual(copy.text(.recoverByEmail), "Recover by email")
    XCTAssertEqual(copy.text(.recoverBySMS), "Recover by SMS")
    XCTAssertEqual(copy.text(.languageOptionSpanish), "ES")
    XCTAssertEqual(copy.text(.languageOptionEnglish), "EN")
    XCTAssertEqual(copy.text(.runtimeStateSectionTitle), "Enterprise runtime state by domain")
    XCTAssertEqual(copy.text(.runtimeStateModeLabel), "Runtime mode")
    XCTAssertEqual(copy.text(.runtimeStateRecoveryAction), "Recover domain")
    XCTAssertEqual(copy.text(.roleLabel), "Role")
    XCTAssertEqual(copy.text(.roleAthlete), "Athlete")
    XCTAssertEqual(copy.text(.roleCoach), "Coach")
    XCTAssertEqual(copy.text(.roleAdmin), "Admin")
    XCTAssertEqual(copy.text(.settingsTitle), "Settings")
    XCTAssertEqual(copy.text(.legalSectionTitle), "Privacy and consent")
    XCTAssertEqual(copy.text(.acceptPrivacyPolicy), "I accept the privacy policy")
    XCTAssertEqual(copy.humanStatus("signed_out"), "signed out")
    XCTAssertEqual(copy.humanStatus("validation_error"), "validation error")
    XCTAssertEqual(copy.humanStatus("consent_required"), "consent required")
    XCTAssertEqual(copy.humanStatus("recovery_sent_email"), "recovery sent by email")
    XCTAssertEqual(copy.humanStatus("recovery_sent_sms"), "recovery sent by sms")
    XCTAssertEqual(copy.humanStatus("exported"), "exported")
    XCTAssertEqual(copy.humanStatus("loading"), "loading")
    XCTAssertEqual(copy.humanStatus("offline"), "offline")
    XCTAssertEqual(copy.humanStatus("denied"), "denied")
  }
}
